package dev.daniloberr;

// Estructuras de control: Bucles for

public class _11BuclesFor {

    public static void main (String[] args) {

        /*
            La estructura de la signatura de un bucle for sería:
                (Condición de comienzo; Condición de parada; Condición de paso)
            El nombre de la variable i podría ser cualquiera, pero es una convención utilizarlo

         */
        for (int i = 0; i < 10; i++) {
            System.out.println(i + " Hola mundo");
        }

        /*
            El bucle for se suele utilizar para iterar arrays
         */
    }
}
