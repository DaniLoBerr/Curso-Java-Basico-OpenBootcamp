package com.sesionesJavaBasico.errores;

public class Warnings {

    /**
     *
     * WARNINGS
     *
     * Son avisos que nos proporciona el IDE sobre partes
     * del código que no son exactamente fallos pero
     * que podrían ser la antesala de los mismos.
     *
     * Se considera buena práctica el limpiar el código
     * de Warnings para tener un código lo más limpio posible.
     *
     * A una mala, existe un elemento llamado "@SuppressWarnings()"
     * que elimina los avisos, aunque lo potenciales fallos
     * seguirían existiendo. Entre los paréntesis se puede indicar
     * el tipo de warning que se quiere suprimir.
     *
     */
}
