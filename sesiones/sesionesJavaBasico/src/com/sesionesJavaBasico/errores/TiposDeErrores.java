package com.sesionesJavaBasico.errores;

public class TiposDeErrores {

    /**
     *
     * ERRORES
     *
     * Los errores son los comportamientos no esperados que ocurren
     * en un programa cuando lo diseñamos, escribimos o
     * ejecutamos y que resultan en un mal funcionamiento del mismo.
     *
     * TIPOS DE ERRORES
     *
     * 1. Error en tiempo de ejecución:
     *
     *  Error que se produce durante la ejecución del programa y que hace
     *  que este aborte la misma. La JVM detecta que se le está
     *  pidiendo una cosa al programa que no puede hacer y lanza una excepción.
     *  Por ejemplo, cuando en una operación aritmética se le pide dividir
     *  caracteres en vez de números.
     *
     *  Existen muchos tipos de excepciones en función del tipo de error.
     *
     *
     *  2. Error en tiempo de compilación:
     *
     *  Fallo que se produce cuando existe algún error sintáctico en el
     *  código que impide ejecutar el programa. Estos errores son detectados
     *  por el compilador y se muestra un mensaje de error en la pantalla
     *  durante la compilación.
     *  Por ejemplo, no cerrar una sentencia con un ;.
     *
     *
     *  3. Error lógico:
     *
     *  Son los errores producidos por las personas. Ocurren cuando
     *  el programa se compila bien y se ejecuta pero devuelve un
     *  resultado incorrecto o que no es el esperado.
     *  Por ejemplo, usar accidentamente un operado incorrecto
     *  (/ en vez de %).
     */

}
