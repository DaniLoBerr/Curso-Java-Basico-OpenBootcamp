package com.ejercicios.ejerciciosJavaBasico.EjercicioTema5;

public class SmartDevice {

    double weight;
    String color;
    int batteryLife;
    double display;

    public SmartDevice(){}

    public SmartDevice(double weight, String color, int batteryLife, double display){
        this.weight = weight;
        this.color = color;
        this.batteryLife = batteryLife;
        this.display = display;
    }

}
