package com.ejercicios.ejerciciosJavaBasico;

public class EjercicioTema2 {

    /**
     * Para este primer ejercicio tendréis que realizar lo siguiente:
     *
     * Crea un proyecto de Java desde 0
     *
     * Dentro del proyecto tenéis que crear un paquete. En el paquete tendréis que crear una clase.
     *
     * Dentro de la clase tenéis que crear el método main e imprimir todos los datos que se han visto en las sesiones.
     *
     * Recordatorio: Los tipos de datos más comunes son int, long, double, boolean, String.
     *
     * @param args
     */

    public static void main(String[] args) {

        // TIPOS DE DATOS

        // Números enteros
        short variableShort = 1;
        System.out.println(variableShort);
        byte variableByte = 2;
        System.out.println(variableByte);
        int variableInt = 3;
        System.out.println(variableInt);
        long variableLong = 4;
        System.out.println(variableLong);

        // Numeros decimales
        float variableFloat = 5.1f;
        System.out.println(variableFloat);
        double variableDouble = 5.2;
        System.out.println(variableDouble);

        // Booleanos
        boolean variableBooleanTrue = true;
        System.out.println(variableBooleanTrue);
        boolean variableBooleanFalse = false;
        System.out.println(variableBooleanFalse);

        // Textos
        char variableChar = 'a';
        System.out.println(variableChar);
        String variableString = "hola";
        System.out.println(variableString);
    }
}
